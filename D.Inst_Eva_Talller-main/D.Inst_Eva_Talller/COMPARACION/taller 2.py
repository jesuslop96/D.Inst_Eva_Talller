#2.	Si a=7 y b=3, ¿Cuál es el resultado de cada una de las siguientes comparaciones?

a, b = 7, 3

Soluc1 = a != b
print(Soluc1)

Soluc2 = a == b
print(Soluc2)

Soluc3 = a > b
print(Soluc3)

Soluc4 = (a+b) < 2
print(Soluc4)

Soluc5 = (a > 7) and (b != 3)
print(Soluc5)

Soluc6 = (a<7) or (b==3)
print(Soluc6)

Soluc7 = (a<7) or (b!=3)
print(Soluc7)